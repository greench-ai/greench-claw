#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
source "$ROOT_DIR/scripts/lib/docker-build.sh"
IMAGE_NAME="${GREENCHCLAW_INSTALL_E2E_IMAGE:-GreenchClaw-install-e2e:local}"
INSTALL_URL="${GREENCHCLAW_INSTALL_URL:-https://GreenchClaw.bot/install.sh}"

OPENAI_API_KEY="${OPENAI_API_KEY:-}"
ANTHROPIC_API_KEY="${ANTHROPIC_API_KEY:-}"
ANTHROPIC_API_TOKEN="${ANTHROPIC_API_TOKEN:-}"
GREENCHCLAW_E2E_MODELS="${GREENCHCLAW_E2E_MODELS:-}"

echo "==> Build image: $IMAGE_NAME"
docker_build_run install-e2e-build \
  -t "$IMAGE_NAME" \
  -f "$ROOT_DIR/scripts/docker/install-sh-e2e/Dockerfile" \
  "$ROOT_DIR/scripts/docker"

echo "==> Run E2E installer test"
docker run --rm \
  -e GREENCHCLAW_INSTALL_URL="$INSTALL_URL" \
  -e GREENCHCLAW_INSTALL_TAG="${GREENCHCLAW_INSTALL_TAG:-latest}" \
  -e GREENCHCLAW_E2E_MODELS="$GREENCHCLAW_E2E_MODELS" \
  -e GREENCHCLAW_INSTALL_E2E_OPENAI_MODEL="${GREENCHCLAW_INSTALL_E2E_OPENAI_MODEL:-}" \
  -e GREENCHCLAW_INSTALL_E2E_OPENAI_PROVIDER_TIMEOUT_SECONDS="${GREENCHCLAW_INSTALL_E2E_OPENAI_PROVIDER_TIMEOUT_SECONDS:-}" \
  -e GREENCHCLAW_INSTALL_E2E_PREVIOUS="${GREENCHCLAW_INSTALL_E2E_PREVIOUS:-}" \
  -e GREENCHCLAW_INSTALL_E2E_SKIP_PREVIOUS="${GREENCHCLAW_INSTALL_E2E_SKIP_PREVIOUS:-0}" \
  -e GREENCHCLAW_INSTALL_E2E_AGENT_TURN_TIMEOUT_SECONDS="${GREENCHCLAW_INSTALL_E2E_AGENT_TURN_TIMEOUT_SECONDS:-300}" \
  -e GREENCHCLAW_INSTALL_E2E_AGENT_TURNS_PARALLEL="${GREENCHCLAW_INSTALL_E2E_AGENT_TURNS_PARALLEL:-1}" \
  -e GREENCHCLAW_INSTALL_E2E_AGENT_TOOL_SMOKE="${GREENCHCLAW_INSTALL_E2E_AGENT_TOOL_SMOKE:-1}" \
  -e GREENCHCLAW_NO_ONBOARD=1 \
  -e OPENAI_API_KEY \
  -e ANTHROPIC_API_KEY \
  -e ANTHROPIC_API_TOKEN \
  "$IMAGE_NAME"
